import fs from "node:fs";
import path from "node:path";
import { createHighlighter } from "shiki";

import { DEMO_SOURCE_FILES } from "../app/demo/demo-sources.config.js";

const ROOT = process.cwd();
const OUT_DIR = path.join(ROOT, "app/demo/generated");
const OUT_FILE = path.join(OUT_DIR, "demo-sources.ts");

/** Strip Shiki inline backgrounds — the panel owns the surface color. */
const transparentSurface = {
  name: "demo-source-surface",
  pre(node) {
    if (node.properties?.style) {
      delete node.properties.style;
    }
  },
};

function escapeTemplate(value) {
  return value.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

function langFor(filePath) {
  if (filePath.endsWith(".tsx")) return "tsx";
  if (filePath.endsWith(".ts")) return "typescript";
  if (filePath.endsWith(".jsx")) return "jsx";
  return "javascript";
}

function highlight(highlighter, code, language, theme) {
  return highlighter.codeToHtml(code, {
    lang: language,
    theme,
    transformers: [transparentSurface],
  });
}

async function buildDemoSources() {
  const highlighter = await createHighlighter({
    themes: ["github-light", "github-dark"],
    langs: ["tsx", "typescript", "javascript", "jsx"],
  });

  /** @type {Record<string, Array<{ name: string; path: string; language: string; code: string; htmlLight: string; htmlDark: string }>>} */
  const bundles = {};

  for (const [demoKey, files] of Object.entries(DEMO_SOURCE_FILES)) {
    bundles[demoKey] = files.map((relPath) => {
      const abs = path.join(ROOT, relPath);
      const code = fs.readFileSync(abs, "utf8");
      const language = langFor(relPath);
      const htmlLight = highlight(highlighter, code, language, "github-light");
      const htmlDark = highlight(highlighter, code, language, "github-dark");

      return {
        name: path.basename(relPath),
        path: relPath.replace(/\\/g, "/"),
        language,
        code,
        htmlLight,
        htmlDark,
      };
    });
  }

  fs.mkdirSync(OUT_DIR, { recursive: true });

  const body = Object.entries(bundles)
    .map(([demoKey, files]) => {
      const entries = files
        .map((file) => {
          return `    {
      name: ${JSON.stringify(file.name)},
      path: ${JSON.stringify(file.path)},
      language: ${JSON.stringify(file.language)},
      code: \`${escapeTemplate(file.code)}\`,
      htmlLight: \`${escapeTemplate(file.htmlLight)}\`,
      htmlDark: \`${escapeTemplate(file.htmlDark)}\`,
    }`;
        })
        .join(",\n");

      return `  ${JSON.stringify(demoKey)}: [\n${entries}\n  ]`;
    })
    .join(",\n");

  const output = `// Auto-generated by scripts/build-demo-sources.js — do not edit.

export type DemoSourceFile = {
  name: string;
  path: string;
  language: string;
  code: string;
  htmlLight: string;
  htmlDark: string;
};

export const DEMO_SOURCES: Record<string, readonly DemoSourceFile[]> = {
${body}
};
`;

  fs.writeFileSync(OUT_FILE, output);
  console.log(
    `[demo-sources] wrote ${Object.keys(bundles).length} bundle(s) → app/demo/generated/demo-sources.ts`,
  );
}

export { buildDemoSources };

const isDirectRun = process.argv[1]?.endsWith("build-demo-sources.js");

if (isDirectRun) {
  buildDemoSources().catch((error) => {
    console.error("[demo-sources] build failed:", error);
    process.exit(1);
  });
}
